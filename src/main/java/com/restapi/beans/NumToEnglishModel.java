package com.restapi.beans;

public class NumToEnglishModel {
	Long Number;
	String NumInEnglish;
	String Status;
	
	public Long getNumber() {
		return Number;
	}
	public void setNumber(Long number) {
		Number = number;
	}
	public String getNumInEnglish() {
		return NumInEnglish;
	}
	public void setNumInEnglish(String numInEnglish) {
		NumInEnglish = numInEnglish;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
}
